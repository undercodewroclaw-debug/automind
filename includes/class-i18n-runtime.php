class I18n_Runtime {

    public static function init() {
        add_filter('gettext', [__CLASS__, 'filter'], 10, 3);
    }

    public static function filter($translated, $text, $domain) {
        if ($domain !== 'automind') return $translated;

        $loc = get_option('automind_locale', 'default');
        if ($loc === 'default' || $loc === 'pl_PL') return $translated;

        static $cache = [];
        if (!isset($cache[$loc])) {
            $base = self::map_en(); // EN fallback
            switch ($loc) {
                case 'en_US': $cache[$loc] = $base; break;
                case 'zh_CN': $cache[$loc] = array_merge($base, self::map_zh_CN()); break;
                case 'hi_IN': $cache[$loc] = array_merge($base, self::map_hi_IN()); break;
                case 'es_ES': $cache[$loc] = array_merge($base, self::map_es_ES()); break;
                case 'fr_FR': $cache[$loc] = array_merge($base, self::map_fr_FR()); break;
                case 'de_DE': $cache[$loc] = array_merge($base, self::map_de_DE()); break;
                case 'pt_BR': $cache[$loc] = array_merge($base, self::map_pt_BR()); break;
                case 'ar':    $cache[$loc] = array_merge($base, self::map_ar()); break;
                case 'ja_JP': $cache[$loc] = array_merge($base, self::map_ja_JP()); break;
                case 'ko_KR': $cache[$loc] = array_merge($base, self::map_ko_KR()); break;
                default:      $cache[$loc] = $base;
            }
        }
        $map = $cache[$loc];

        return isset($map[$text]) ? $map[$text] : $translated;
    }

